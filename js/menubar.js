
$("#about").hoverIntent(function(){
    $('#hiddenabout').fadeIn();
    $("#hiddenabout").animate({height:'200px'});
    $('#about').css("background-color","white");
    $('#about').css("color","green");
},function(){
    $("#hiddenabout").animate({height:'0px'});
    $('#hiddenabout').fadeOut();
    $('#about').css("background-color","transparent");
    $('#about').css("color","#C4C4C4");
});

